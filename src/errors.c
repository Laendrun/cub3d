#include "cub3d.h"

int	puterr(char *s1, char *s2)
{
	while (s1 && *s1)
		write(2, s1++, 1);
	while (s2 && *s2)
		write(2, s2++, 1);
	write(2, "\n", 1);
	return (1);
}
